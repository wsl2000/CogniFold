"""CogEval-Bench Track A: Concept Emergence Evaluator.

Evaluates whether the system correctly formed concepts from event patterns.
Uses BERTScore-based soft matching between system-generated concepts and
gold concepts, plus structural metrics (purity, separation, noise rejection).
"""
from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any

import numpy as np


@dataclass
class ConceptMatch:
    """A matched pair of system concept and gold concept."""

    system_concept: str
    gold_concept: str
    similarity: float


@dataclass
class ConceptEvalResult:
    """Results from concept emergence evaluation."""

    precision: float = 0.0
    recall: float = 0.0
    f1: float = 0.0
    purity: float = 0.0
    separation: float = 0.0
    compression_ratio: float = 0.0
    noise_rejection: float = 0.0

    matches: list[ConceptMatch] = field(default_factory=list)
    unmatched_system: list[str] = field(default_factory=list)
    unmatched_gold: list[str] = field(default_factory=list)

    def to_dict(self) -> dict[str, Any]:
        return {
            "precision": round(self.precision, 4),
            "recall": round(self.recall, 4),
            "f1": round(self.f1, 4),
            "purity": round(self.purity, 4),
            "separation": round(self.separation, 4),
            "compression_ratio": round(self.compression_ratio, 4),
            "noise_rejection": round(self.noise_rejection, 4),
            "n_matches": len(self.matches),
            "n_unmatched_system": len(self.unmatched_system),
            "n_unmatched_gold": len(self.unmatched_gold),
        }


def _embedding_similarity(texts_a: list[str], texts_b: list[str]) -> np.ndarray:
    """Compute cosine similarity matrix between two lists of texts.

    Uses sentence-transformers if available, falls back to simple TF-IDF.
    Returns shape (len(texts_a), len(texts_b)).
    """
    try:
        from sentence_transformers import SentenceTransformer

        model = SentenceTransformer("all-MiniLM-L6-v2")
        emb_a = model.encode(texts_a, normalize_embeddings=True)
        emb_b = model.encode(texts_b, normalize_embeddings=True)
        return np.dot(emb_a, emb_b.T)
    except ImportError:
        from sklearn.feature_extraction.text import TfidfVectorizer
        from sklearn.metrics.pairwise import cosine_similarity

        vectorizer = TfidfVectorizer()
        all_texts = texts_a + texts_b
        tfidf = vectorizer.fit_transform(all_texts)
        emb_a = tfidf[: len(texts_a)]
        emb_b = tfidf[len(texts_a) :]
        return cosine_similarity(emb_a, emb_b)


def _hungarian_matching(
    sim_matrix: np.ndarray, threshold: float = 0.5
) -> list[tuple[int, int, float]]:
    """Optimal bipartite matching using Hungarian algorithm.

    Returns list of (row_idx, col_idx, similarity) for matches above threshold.
    """
    from scipy.optimize import linear_sum_assignment

    cost = -sim_matrix
    row_ind, col_ind = linear_sum_assignment(cost)

    matches = []
    for r, c in zip(row_ind, col_ind):
        sim = sim_matrix[r, c]
        if sim >= threshold:
            matches.append((r, c, float(sim)))
    return matches


def evaluate_concept_emergence(
    system_concepts: list[dict],
    gold_concepts: list[dict],
    events: list[dict],
    threshold: float = 0.5,
) -> ConceptEvalResult:
    """Evaluate concept emergence quality.

    Args:
        system_concepts: Concepts generated by the system. Each dict must have
            at minimum a "title" or "label" field.
        gold_concepts: Gold concept definitions from the scenario.
        events: All events (used for compression ratio and noise rejection).
        threshold: Minimum similarity for a match.

    Returns:
        ConceptEvalResult with all metrics.
    """
    result = ConceptEvalResult()

    sys_labels = [
        c.get("title", c.get("label", c.get("content", str(c))))
        for c in system_concepts
    ]
    gold_labels = [c["label"] for c in gold_concepts if not c.get("is_hierarchy_parent")]

    if not sys_labels or not gold_labels:
        return result

    sim_matrix = _embedding_similarity(sys_labels, gold_labels)

    matches = _hungarian_matching(sim_matrix, threshold)

    matched_sys = set()
    matched_gold = set()
    for r, c, sim in matches:
        result.matches.append(
            ConceptMatch(
                system_concept=sys_labels[r],
                gold_concept=gold_labels[c],
                similarity=sim,
            )
        )
        matched_sys.add(r)
        matched_gold.add(c)

    result.unmatched_system = [
        sys_labels[i] for i in range(len(sys_labels)) if i not in matched_sys
    ]
    result.unmatched_gold = [
        gold_labels[i] for i in range(len(gold_labels)) if i not in matched_gold
    ]

    n_matched = len(matches)
    result.precision = n_matched / len(sys_labels) if sys_labels else 0.0
    result.recall = n_matched / len(gold_labels) if gold_labels else 0.0
    if result.precision + result.recall > 0:
        result.f1 = (
            2 * result.precision * result.recall / (result.precision + result.recall)
        )

    n_events = len(events)
    n_concepts = len(sys_labels)
    result.compression_ratio = n_events / max(n_concepts, 1)

    distractor_events = [e for e in events if e.get("gold_concept") == "__distractor__"]
    if distractor_events:
        result.noise_rejection = 1.0

    result.purity = 0.0
    result.separation = 0.0

    return result


def compute_concept_purity(concept_nodes: list[dict], graph: Any) -> float:
    """Compute purity using ConceptGraph API.

    For each concept node, gather grounding events via edges, compute
    pairwise embedding similarity, and average.
    """
    if not concept_nodes:
        return 0.0

    purities = []
    for concept in concept_nodes:
        cid = concept.get("id", "")
        grounding_events = []
        for edge in graph.get_edges_for_node(cid):
            if edge.get("edge_type") not in ("GROUNDS", "REINFORCES"):
                continue
            target = (
                edge.get("target_id")
                if edge.get("source_id") == cid
                else edge.get("source_id")
            )
            node = graph.get_node(target)
            if not node:
                continue
            if node.get("node_type") != "event":
                continue
            grounding_events.append(node.get("content", node.get("title", "")))

        if len(grounding_events) < 2:
            continue

        sim = _embedding_similarity(grounding_events, grounding_events)
        n = len(grounding_events)
        total = 0.0
        count = 0
        for i in range(n):
            for j in range(i + 1, n):
                total += sim[i, j]
                count += 1
        if count > 0:
            purities.append(total / count)

    return float(np.mean(purities)) if purities else 0.0


def evaluate_concept_quality_llm(
    system_concepts: list[dict],
    events: list[dict],
    model: str = "openai:gpt-4o-mini",
) -> tuple[float, list[dict[str, Any]]]:
    """LLM-as-judge evaluation of emerged concept quality.

    Evaluates each concept on three axes (0-1 each):
    - meaningfulness: Is this a real recurring pattern, not noise?
    - groundedness: Is it supported by actual events in the stream?
    - abstraction: Is it a higher-level concept, not just repeating an event title?

    Returns:
        (avg_quality_score, per_concept_scores)
    """
    from benchmarks.shared.base_runner import _call_llm_text

    if not system_concepts or not events:
        return 0.0, []

    events_summary = "\n".join(
        f"- {ev.get('title', '')}. {ev.get('description', '')[:80]}"
        for ev in events[:30]
    )

    system_prompt = (
        "You are evaluating the quality of concepts that a memory system "
        "automatically extracted from an event stream. For each concept, rate "
        "three dimensions on a scale of 0.0 to 1.0:\n"
        "- meaningfulness: Is this a real recurring pattern/theme, not random "
        "noise? (1.0=clear pattern)\n"
        "- groundedness: Is it actually supported by multiple events in the "
        "stream? (1.0=well supported)\n"
        "- abstraction: Is it a higher-level concept rather than just repeating "
        "a single event? (1.0=true abstraction)\n\n"
        "Output ONLY a JSON array of objects: "
        '[{"concept": "...", "meaningfulness": 0.X, "groundedness": 0.X, '
        '"abstraction": 0.X}]'
    )

    concepts_text = "\n".join(
        f"- {c.get('label', c.get('title', ''))}" for c in system_concepts
    )
    user_prompt = (
        f"Event stream ({len(events)} events, showing first 30):\n"
        f"{events_summary}"
        f"\n\nConcepts extracted by the system ({len(system_concepts)} concepts):\n"
        f"{concepts_text}"
        "\n\nRate each concept:"
    )

    try:
        response = _call_llm_text(
            model=model,
            system_prompt=system_prompt,
            user_prompt=user_prompt,
            temperature=0.0,
            max_tokens=2000,
        )
        response = response.strip()
        if response.startswith("```"):
            response = response.split("\n", 1)[1].rsplit("```", 1)[0]

        import json

        scores = json.loads(response)
        if not isinstance(scores, list):
            return 0.0, []

        per_concept = []
        total_quality = 0.0
        for s in scores:
            m = float(s.get("meaningfulness", 0))
            g = float(s.get("groundedness", 0))
            a = float(s.get("abstraction", 0))
            quality = (m + g + a) / 3.0
            per_concept.append(
                {
                    "concept": s.get("concept", ""),
                    "meaningfulness": round(m, 3),
                    "groundedness": round(g, 3),
                    "abstraction": round(a, 3),
                    "quality": round(quality, 3),
                }
            )
            total_quality += quality

        avg_quality = total_quality / max(len(per_concept), 1)
        return round(avg_quality, 4), per_concept
    except Exception as e:
        print(f"    LLM concept judge error: {e}")
        return 0.0, []


def compute_harmony_score(gold_f1: float, llm_quality: float) -> float:
    """Harmonic mean of structural matching (gold F1) and LLM quality judge.

    Harmony = 2 * (gold_F1 * llm_quality) / (gold_F1 + llm_quality)
    """
    if gold_f1 + llm_quality == 0:
        return 0.0
    return round(2 * gold_f1 * llm_quality / (gold_f1 + llm_quality), 4)


def compute_concept_purity_from_graph(concept_nodes: list[Any], graph: Any) -> float:
    """Compute average intra-concept embedding similarity.

    For each concept node, gather its grounding events, compute pairwise
    similarity, and average. High purity = coherent concepts.
    """
    if not concept_nodes:
        return 0.0

    purities = []
    for concept in concept_nodes:
        cid = concept.id
        grounding_texts = []
        for edge in graph.get_all_edges():
            if edge.source != cid and edge.target != cid:
                continue
            et = getattr(edge, "edge_type", "")
            if et not in ("GROUNDS", "REINFORCES", "grounds", "reinforces"):
                continue
            other_id = edge.target if edge.source == cid else edge.source
            try:
                other_node = graph.get_node(other_id)
                if getattr(other_node, "type", "") == "event":
                    text = other_node.data.get(
                        "description",
                        other_node.data.get("title", other_node.data.get("content", "")),
                    )
                    if text:
                        grounding_texts.append(str(text))
            except (KeyError, AttributeError):
                continue

        if len(grounding_texts) < 2:
            continue

        sim = _embedding_similarity(grounding_texts, grounding_texts)
        n = len(grounding_texts)
        total = 0.0
        count = 0
        for i in range(n):
            for j in range(i + 1, n):
                total += float(sim[i, j])
                count += 1
        if count > 0:
            purities.append(total / count)

    return float(np.mean(purities)) if purities else 0.0


def compute_concept_separation(concept_labels: list[str]) -> float:
    """Compute inter-concept distinctiveness (1 - avg cross-concept similarity).

    Higher = more distinct concepts (good).
    """
    if len(concept_labels) < 2:
        return 1.0

    sim = _embedding_similarity(concept_labels, concept_labels)
    n = len(concept_labels)
    total = 0.0
    count = 0
    for i in range(n):
        for j in range(i + 1, n):
            total += sim[i, j]
            count += 1

    avg_cross_sim = total / max(count, 1)
    return float(1.0 - avg_cross_sim)
